CREATE TABLE PRODUTO
(
	 ID_PRODUTO TINYINT IDENTITY NOT NULL
	,DESCRICAO VARCHAR(100) NOT NULL
	,QTD INT NOT NULL
	,ID_MARCA TINYINT NOT NULL
	,ID_CATEGORIA TINYINT NOT NULL
	,VALOR DECIMAL(8,2) NOT NULL
)
GO

CREATE TABLE MARCA
(
	 ID_MARCA TINYINT IDENTITY NOT NULL
	,MARCAR VARCHAR(60) NOT NULL
)

CREATE TABLE CATEGORIA
(
	ID_CATEGORIA TINYINT IDENTITY NOT NULL
	,CATEGORIA VARCHAR(60)
)

CREATE TABLE PEDIDO_VENDA
(
	 COD_VENDA INT NOT NULL
	,COD_SEQ INT NOT NULL
	,ID_VENDEDOR VARCHAR(60) NOT NULL
	,ID_PRODUTO TINYINT NOT NULL
	,ID_CLIENTE TINYINT NOT NULL
